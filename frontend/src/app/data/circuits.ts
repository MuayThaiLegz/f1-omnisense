/** Lookup by anomaly_scores.json race name (e.g. "Abu Dhabi") */
export function getCircuit(raceName: string): CircuitInfo | undefined {
  return CIRCUITS.find(c => c.race === raceName);
}

/** Lookup by OpenF1 circuit_short_name (e.g. "Yas Marina Circuit", "Catalunya") */
export function getCircuitByShortName(shortName: string): CircuitInfo | undefined {
  return CIRCUITS.find(c => c.shortNames.includes(shortName));
}
